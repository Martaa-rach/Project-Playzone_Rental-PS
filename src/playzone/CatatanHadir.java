    private void jButton1checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1checkActionPerformed
    String id_pegawai = SessionLogin.getID_pegawai(); // ambil dari session login
    String nama = "";
    String shift = (String) jComboBox1.getSelectedItem(); // ambil shift yang dipilih user
    String status = "Hadir";
    String id_Presensi = generateAutoID("presensi", "id_presensi", "PRS");

    // Ambil nama pegawai dari database
    try {
        
        String sql = "SELECT nama FROM pegawai WHERE id_pegawai = ?";
        PreparedStatement pst = cobakonek.prepareStatement(sql);
        pst.setString(1, id_pegawai);

        ResultSet rs = pst.executeQuery();
        if (rs.next()) {
            nama = rs.getString("nama");
        } else {
            JOptionPane.showMessageDialog(this, "Pegawai tidak ditemukan");
            return; // stop proses kalau pegawai tidak ditemukan
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal mengambil nama pegawai: " + e.getMessage());
        return;
    }

    // Simpan ke tabel presensi (tanggal otomatis pakai NOW())
    try {
        String sql = "INSERT INTO presensi (ID_presensi, ID_pegawai, nama, shift, tanggal, status) VALUES (?, ?, ?, ?, NOW(), ?)";
        PreparedStatement pst = cobakonek.prepareStatement(sql);
        pst.setString(1, id_Presensi);
        pst.setString (2, id_pegawai);
        pst.setString(3, nama);
        pst.setString(4, shift);
        pst.setString(5, status);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Check-In berhasil!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal Check-In: " + e.getMessage());
    }

    loadDataToTable(); // update isi jTable

    }//GEN-LAST:event_jButton1checkActionPerformed

