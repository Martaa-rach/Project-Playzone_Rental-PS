   private void loadDataToTable() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // reset isi tabel

    String idPegawai = SessionLogin.getID_pegawai(); // ambil ID dari session login

    try {
        String sql = "SELECT ID_presensi, ID_pegawai, nama, shift, tanggal, status FROM presensi WHERE ID_pegawai = ? ORDER BY tanggal DESC";
        PreparedStatement pst = cobakonek.prepareStatement(sql);
        pst.setString(1, idPegawai); // set parameter id_pegawai

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            String idPresensi = rs.getString("ID_presensi");
            String idPegawaiResult = rs.getString("ID_pegawai");
            String nama = rs.getString("nama");
            String shift = rs.getString("shift");
            String tanggal = rs.getString("tanggal");
            String status = rs.getString("status");

            model.addRow(new Object[]{idPresensi, idPegawaiResult, nama, shift, tanggal, status});
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load tabel: " + e.getMessage());
    }
}



