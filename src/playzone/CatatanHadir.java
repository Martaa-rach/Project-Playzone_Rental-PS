        private String generateAutoID(String presensi, String ID_presensi, String PRS) {
            String newID = "";
            try {
                String sql = "SELECT MAX(" + ID_presensi + ") FROM " + presensi;
                PreparedStatement pst = cobakonek.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    String maxID = rs.getString(1);
                    if (maxID != null && maxID.startsWith(PRS)) {
                        int number = Integer.parseInt(maxID.substring(PRS.length())) + 1;
                        newID = PRS + number; 
                    } else {
                        newID = PRS + "1";
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error generate ID: " + e.getMessage());
            }
            return newID;
        }


